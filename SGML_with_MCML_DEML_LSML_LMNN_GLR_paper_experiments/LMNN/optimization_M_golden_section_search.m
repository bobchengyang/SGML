function [gamma] = optimization_M_golden_section_search(...
    xL,...
    xU,...
    M_previous,...
    t_M21_solution_previous,...
    M_lc,...
    partial_feature,...
    feature_N,...
    BCD,...
    remaining_idx,...
    tol_golden_search,...
    zero_mask,...
    zz,...
    total_offdia,...
    partial_sample,...
    targets_ind,...
    same_label,...
    dia_idx,...
    nv)

% fL=optimization_M_get_function_value(...
%     xL,...
%     M_previous,...
%     t_M21_solution_previous,...
%     M_lc,...
%     partial_feature,...
%     x,...
%     feature_N,...
%     BCD);

% fU=optimization_M_get_function_value(...
%     xU,...
%     M_previous,...
%     t_M21_solution_previous,...
%     M_lc,...
%     partial_feature,...
%     x,...
%     feature_N,...
%     BCD);

R=0.5*(sqrt(5)-1);
d=R*(xU-xL);
x1=xU-d;
x2=xL+d;

f1=optimization_M_get_function_value(...
    x1,...
    M_previous,...
    t_M21_solution_previous,...
    M_lc,...
    partial_feature,...
    feature_N,...
    BCD,...
    remaining_idx,...
    zero_mask,...
    zz,...
    total_offdia,...
    partial_sample,...
    targets_ind,...
    same_label,...
    dia_idx,...
    nv);

f2=optimization_M_get_function_value(...
    x2,...
    M_previous,...
    t_M21_solution_previous,...
    M_lc,...
    partial_feature,...
    feature_N,...
    BCD,...
    remaining_idx,...
    zero_mask,...
    zz,...
    total_offdia,...
    partial_sample,...
    targets_ind,...
    same_label,...
    dia_idx,...
    nv);

err=Inf;
ni=0;
while err>tol_golden_search
    ni=ni+1;
    if f1<f2
        xU=x2;
        %fU=f2;
        x2=x1;
        f2=f1;
        d=R*(xU-xL);
        x1=xU-d;
        f1=optimization_M_get_function_value(...
            x1,...
            M_previous,...
            t_M21_solution_previous,...
            M_lc,...
            partial_feature,...
            feature_N,...
            BCD,...
            remaining_idx,...
            zero_mask,...
            zz,...
            total_offdia,...
            partial_sample,...
            targets_ind,...
            same_label,...
            dia_idx,...
            nv);
        
    elseif f1>f2
        xL=x1;
        %fL=f1;
        x1=x2;
        f1=f2;
        d=R*(xU-xL);
        x2=xL+d;
        f2=optimization_M_get_function_value(...
            x2,...
            M_previous,...
            t_M21_solution_previous,...
            M_lc,...
            partial_feature,...
            feature_N,...
            BCD,...
            remaining_idx,...
            zero_mask,...
            zz,...
            total_offdia,...
            partial_sample,...
            targets_ind,...
            same_label,...
            dia_idx,...
            nv);
    else
        xL=(x1+x2)/2;
        xU=xL;
    end
    err=2*abs(xU-xL)/(xU+xL);
end
gamma=(x1+x2)/2;
%disp([num2str(ni) ' golden section search iterations']);
end

